# Esta aula é sobre String

"""
str - string

strings são textos que estão dentro de aspas
"""

print('Um Print')  # Essas aspas simples, duplas, triplas, são strings
print('Aspas Simples')
print("Aspas Duplas")

print("Esta é uma 'String' (str).")  # Exemplo utilizando aspas simpes e duplas
print('Esta é uma "String" (str).')
print("Esta é uma \"String\" (str).")  # Utilizando caractere de escape
print('Esta é uma \'String\' (str).')  # Barra invertida é o caractere de escape

print('Esta é uma \n (str).')
print(r'Esta é uma \n (str).')  # Colocando o r (raw) ele significa que o texto não é para nada dentro dele ser executado, pois roda a string bruta
print(r'Esta é uma \'String\' (str).')  # Exemplo, para ver que ele ignora tudo, rodando a string bruta, mostando as barras e aspas
