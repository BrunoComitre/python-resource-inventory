# Esta aula é sobre operadores aritméticos

"""
+    = mais
-    = menos
*    = multiplicação
/    = divisão
'//' = divisão inteira - essas aspas são apenas para conseguir ver as duas barras 
**   = exponenciação
%    = resto da divisão
()   = parênteses
"""

print(10 * 10)  # Multiplicação
print(10 + 10)  # Adição
print(10 - 5)  # Subtração
print(10 / 2)  # Divisão

print('Bruno' + ' ' + 'Alves' + ' ' + 'Comitre')  # Exemplo usando o mais para gerar espaço

print(10.0 // 3)  # Divisão Inteira
print(10.0 / 3)  # Divisão Normal

print(2 ** 10)  # Dois elevado a 10
print(10 % 5)  # Resto da Divisão
print(10 % 3)  # Resto da Divisão

print(5 + 2 * 10)  # Explicando o Parênteses
print((5 + 2) * 10)  # Exemplo de funcionamento do parênteses
